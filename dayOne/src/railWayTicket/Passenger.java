package railWayTicket;

public class Passenger {
	String name;
	int age;
	String gender;
	String bp;
	int pnr;
	int price;
	int PnrNo;
	public Passenger(String name,int age,String gender,String bp,int pnr,int price,int PnrNo) {
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.bp = bp;
		this.pnr=pnr;
		this.price=price;
		this.PnrNo=PnrNo;
	}	
}
